<template>
  <div class="poster-page-bg" :style="posterStyle"></div>
</template>

<script>
export default {
  name: "PosterBg",
  props: {
    poster: {
      type: String,
      default: ''
    }
  },
  data() {
    return {
      defaultPoster: 'linear-gradient(45deg, rgb(0, 3, 38) 0%, rgb(82, 15, 117) 100%)'
    }
  },
  computed: {
    posterBg() {
      return this.poster ? `url(${this.poster})` : this.defaultPoster
    },
    posterStyle() {
      return {
        'background-image': this.posterBg
      }
    }
  }
}
</script>

